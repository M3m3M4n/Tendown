unsigned int sub_calc_crc(unsigned int sum, char* buf, unsigned int size){
	if(buf != 0){
		unsigned int temp0, temp1, temp2;
		char* temp3;
		temp1 = ~sum;
                temp2 = size;
                temp3 = buf;
		while(temp2 > 7){
                	temp0 = *(unsigned int *)(crcbytes + ((*temp3 ^ temp1) & 0xff) * 4) ^ temp1 >> 8;
			temp0 = *(unsigned int *)(crcbytes + ((temp3[1] ^ temp0) & 0xff) * 4) ^ temp0 >> 8;
      			temp0 = *(unsigned int *)(crcbytes + ((temp3[2] ^ temp0) & 0xff) * 4) ^ temp0 >> 8;
      			temp0 = *(unsigned int *)(crcbytes + ((temp3[3] ^ temp0) & 0xff) * 4) ^ temp0 >> 8;
      			temp0 = *(unsigned int *)(crcbytes + ((temp3[4] ^ temp0) & 0xff) * 4) ^ temp0 >> 8;
      			temp0 = *(unsigned int *)(crcbytes + ((temp3[5] ^ temp0) & 0xff) * 4) ^ temp0 >> 8;
      			temp0 = *(unsigned int *)(crcbytes + ((temp3[6] ^ temp0) & 0xff) * 4) ^ temp0 >> 8;
      			temp1 = *(unsigned int *)(crcbytes + ((temp3[7] ^ temp0) & 0xff) * 4) ^ temp0 >> 8;
      			temp3 = temp3 + 8;
      			temp2 = temp2 - 8;
		}
		while (temp2 != 0) {
			temp1 = *(unsigned int *)(crcbytes + ((*temp3 ^ temp1) & 0xff) * 4) ^ temp1 >> 8;
			temp3 = temp3 + 1;
			temp2 = temp2 - 1;
		}
		temp1 = ~temp1;
		return temp1;
	}
	return 0;
}


unsigned int calc_crc(FILE** fs, unsigned int size){
	unsigned int current_size = 0;
	unsigned int sum = 0;
	char buf[0x2000];
	fseek(*fs, 64, SEEK_SET);
	while(current_size < size){
		unsigned int read = size - current_size;
		if(read > 0x2000){
			read = 0x2000;
		}
		unsigned int t = fread(buf, 1, read, *fs);
		if(t <= 0){
			break;
		}
		current_size += t;
		sum = sub_calc_crc(sum, buf, t);
	}
	return sum;
}

unsigned int calc_rootfs_checksum(FILE ** rootfs){
	// file opened & seek top start of root partition;
	// caller open & close
	unsigned long position = ftell(*rootfs);
	fseek(*rootfs, position + 8, SEEK_SET);
	unsigned int size;
	unsigned short temp;
	fread(&size, 1, 4, *rootfs);
	size = DWORD_SWAP(size);
	size += 0x282;
	fseek(*rootfs, position, SEEK_SET);
	unsigned int sum = 0;
	unsigned int counter = 0;
	while(fread(&temp, 1, 2, *rootfs) == 2){
		counter += 2;
		if(counter > size){
			break;
		}
        	sum += WORD_SWAP(temp);
	}
	return sum;
}

