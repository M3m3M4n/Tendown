int kernel_and_root_post_merge_check(char* name){
	FILE * fs = fopen(name, "rb");	
	unsigned int checksum = calc_kernelfs_checksum(&fs);
	int ret = 0;
	if(((checksum & 0xffff) != 0) || (((checksum & 0xffff0000) >> 16) == 0)){
        	printf("Final kernel checksum failed!, checksum:0x%x\n", checksum);
		ret = -1;
	}
	if(ret >= 0){
		fseek(fs, -2, SEEK_CUR);
		checksum = calc_rootfs_checksum(&fs);
		if(((checksum & 0xffff) != 0) || (((checksum & 0xffff0000) >> 16) == 0)){
                	printf("Final rootfs checksum failed!, checksum:0x%x\n", checksum);
                	ret = -1;
		}
	}
	fclose(fs);
	return ret;
}

