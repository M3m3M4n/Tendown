int pre_check_kernel(char* kernel){
	if( access( kernel, F_OK ) != -1 ) {
		FILE* kernelfs = fopen(kernel, "rb");
		unsigned int val = calc_kernelfs_checksum(&kernelfs);
                if(((val & 0xffff )!= 0) || (((val & 0xffff0000) >> 16) == 0)){
			printf("Kernel checksum failed!\n");
			return -1;
		}
		printf("Kernel check success, full checksum: 0x%x\n", val);
		fclose(kernelfs);
		return 0;
        }
	else{
		printf("Cannot access kernel image!\n");
		return -1;
	}
}

int main(int argc, char* argv[]){
	if(argc != 4){
		printf("Usage: mkimage rootfs_dir kernel.img flash_file_size\nEG: mkimage squashfs-root KernelFS.bin 0x7b0000\n");
		exit(-1);
	}
	printf("Remove old files!\n");
	FILE * rm = popen("rm rootfs.bin modded_rootfs.bin final.bin upgrade.bin", "r");
	while (pclose(rm) <0 ){}
	rm = 0;
	if( pre_check_kernel(argv[2]) < 0 ) {
		printf("Check kernel failed!\n");
		exit(-1);
	}
	if(prepare_rootfs(argv[1])<0){
		printf("Prepare rootfs failed!\n");
		exit(-1);
	}
        if(merge(argv[2], argv[3]) < 0){
		printf("Merge failed!\n");
		exit(-1);
	}
	printf("Cleaning!\n");
	rm = popen("rm rootfs.bin modded_rootfs.bin", "r");
	while (pclose(rm) < 0){}
	printf("Done -> final.bin, upgrade.bin\n");
	return 0;
}
